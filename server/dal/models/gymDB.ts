export default (sequelize, DataTypes) => {
    const Gym = sequelize.define('gym', {
        name: {
          type: DataTypes.STRING
        },
        city: {
          type: DataTypes.STRING
        },
        country: {
          type: DataTypes.STRING
        },
        photo: {
          type: DataTypes.BLOB
        }
      });

    return Gym;
};